// 构建词典 Set（用于快速查找）
const DICT = new Set<string>()
for (const list of [TECH_WORDS, DAILY_WORDS, EMOTION_WORDS]) {
  for (const w of list) DICT.add(w)
}

// 最大词长
const MAX_WORD_LEN = 5

// ─── 正向最大匹配分词 ───

function forwardMaxMatch(text: string): string[] {
  const tokens: string[] = []
  let i = 0
  while (i < text.length) {
    let matched = false
    // 从最长词开始尝试匹配
    for (let len = Math.min(MAX_WORD_LEN, text.length - i); len >= 2; len--) {
      const candidate = text.slice(i, i + len)
      if (DICT.has(candidate)) {
        tokens.push(candidate)
        i += len
        matched = true
        break
      }
    }
    if (!matched) {
      // 单字（unigram），跳过停用词和标点
      const ch = text[i]
      if (/[\u4e00-\u9fff]/.test(ch) && !CHINESE_STOPWORDS.has(ch)) {
        tokens.push(ch)
      }
      i++
    }
  }
  return tokens
}

/** 中文分词（正向最大匹配） */
export function tokenizeChinese(text: string): string[] {
  // 提取纯中文部分
  const chinese = text.replace(/[^\u4e00-\u9fff]/g, '')
  if (chinese.length === 0) return []
  return forwardMaxMatch(chinese)
}

/** 中英文混合分词 */
export function tokenizeMixed(text: string): string[] {
  const tokens: string[] = []
  const lower = text.toLowerCase()

  // 英文词提取
  for (const m of lower.matchAll(/[a-z][a-z0-9_]{1,}/g)) {
    tokens.push(m[0])
  }

  // 中文部分：按非中文字符分割，每段独立分词
  const segments = text.split(/[^\u4e00-\u9fff]+/)
  for (const seg of segments) {
    if (seg.length > 0) {
      tokens.push(...forwardMaxMatch(seg))
    }
  }

  return tokens
}

/** 中英文混合分词（去重，返回 Set） */
export function tokenizeToSet(text: string): Set<string> {
  return new Set(tokenizeMixed(text))
}
